package codebro;

public class wrapperclass17 {

	public static void main(String[] args) {
		// wrapper class = provides a way to use primitive data types as reference data types.
		// AUTOBOXING = converting primitive data types to wrapper class
		// UNBOXING = is the opposite
// PRIMITIVE	// wrapper class
//	int				Integer
//	boolean			Boolean
//	double 			Double
//	char			Character
		
// wrapper classes
		
	Boolean a = true;
	Character b = '@';
	Integer c = 123;
	Double d = 3.14;
	String e = "ndende";
	
// unboxing 
	
	if(a==true) {
		System.out.println("This is true");
	}
	
	
	}

}
